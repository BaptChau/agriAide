<?php

namespace App\Entity;

use ApiPlatform\Core\Annotation\ApiResource;
use App\Repository\PostRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: PostRepository::class)]
#[ApiResource]
class Post
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column(type: 'integer')]
    private $id;

    #[ORM\Column(type: 'string', length: 255)]
    private $title;

    #[ORM\Column(type: 'text')]
    private $corpse;

    #[ORM\Column(type: 'datetime_immutable')]
    private $createdAt;

    #[ORM\Column(type: 'datetime_immutable')]
    private $updatedAt;

    #[ORM\OneToMany(mappedBy: 'postId', targetEntity: Comment::class)]
    private $comments;

    #[ORM\ManyToOne(targetEntity: PostCategory::class, inversedBy: 'posts')]
    #[ORM\JoinColumn(nullable: false)]
    private $categoryId;

    #[ORM\ManyToOne(targetEntity: PostSubCategory::class, inversedBy: 'posts')]
    private $subCategoryId;

    public function __construct()
    {
        $this->comments = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getTitle(): ?string
    {
        return $this->title;
    }

    public function setTitle(string $title): self
    {
        $this->title = $title;

        return $this;
    }

    public function getCorpse(): ?string
    {
        return $this->corpse;
    }

    public function setCorpse(string $corpse): self
    {
        $this->corpse = $corpse;

        return $this;
    }

    public function getCreatedAt(): ?\DateTimeImmutable
    {
        return $this->createdAt;
    }

    public function setCreatedAt(\DateTimeImmutable $createdAt): self
    {
        $this->createdAt = $createdAt;

        return $this;
    }

    public function getUpdatedAt(): ?\DateTimeImmutable
    {
        return $this->updatedAt;
    }

    public function setUpdatedAt(\DateTimeImmutable $updatedAt): self
    {
        $this->updatedAt = $updatedAt;

        return $this;
    }

    /**
     * @return Collection<int, Comment>
     */
    public function getComments(): Collection
    {
        return $this->comments;
    }

    public function addComment(Comment $comment): self
    {
        if (!$this->comments->contains($comment)) {
            $this->comments[] = $comment;
            $comment->setPostId($this);
        }

        return $this;
    }

    public function removeComment(Comment $comment): self
    {
        if ($this->comments->removeElement($comment)) {
            // set the owning side to null (unless already changed)
            if ($comment->getPostId() === $this) {
                $comment->setPostId(null);
            }
        }

        return $this;
    }

    public function getCategoryId(): ?PostCategory
    {
        return $this->categoryId;
    }

    public function setCategoryId(?PostCategory $categoryId): self
    {
        $this->categoryId = $categoryId;

        return $this;
    }

    public function getSubCategoryId(): ?PostSubCategory
    {
        return $this->subCategoryId;
    }

    public function setSubCategoryId(?PostSubCategory $subCategoryId): self
    {
        $this->subCategoryId = $subCategoryId;

        return $this;
    }
}
